# A function is something which process and manipulates the given data and show the result
# here we will pass values in function by the arguments
def add(a,b):
    print(a+b)
# now we will pass arguments in a and b here a=4 and b=8 
add(4,8)
# to pass different value we will just change the set of values in the arguments
add(100, 200)

# Now what if you want to multilpy values by passing arguments
def mul(x,y):
    print(x*y)
# now we will pass arguments in a and b here a=5 and b=5
mul(5, 5)
 